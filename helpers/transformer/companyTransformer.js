const human = require("human-time");

const companyTransformer = ({
  _id,
  name,
  avatarURL,
  description,
  industry,
  location,
  website,
  email,
  phone,
  foundedYear,
  employeesCount,
  owners,
  workers,
  jobs,
  publications,
  createdAt,
  updatedAt,
}) => ({
  _id,
  name,
  avatarURL,
  description,
  industry,
  location,
  website,
  email,
  phone,
  foundedYear,
  employeesCount,
  owners,
  workers,
  jobs,
  publications,
  postedAtHuman: human(createdAt),
  createdAt,
  updatedAt,
});

module.exports = companyTransformer;
